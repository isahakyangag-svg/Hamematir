i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources,
    lng: localStorage.getItem('i18nextLng') || 'ru',
    fallbackLng: ['am', 'en', 'ru'],
    saveMissing: true,
    interpolation: {
      escapeValue: false,
    },
    detection: {
      order: ['querystring', 'cookie', 'localStorage', 'sessionStorage', 'navigator', 'htmlTag'],
      caches: ['localStorage', 'cookie'],
    },
    react: {
      useSuspense: false
    }
  });

// Handle missing keys - Enterprise Level Auto-Detection
i18n.on('missingKey', async (lngs, namespace, key, res) => {
  const docId = 't_' + key.replace(/[^a-zA-Z0-9]/g, '_');
  try {
    const docRef = doc(db, 'translations', docId);
    const docSnap = await getDoc(docRef);
    
    if (!docSnap.exists()) {
      await setDoc(docRef, {
        key,
        category: 'other',
        am: '',
        en: res || key, // Fallback to provided resource or key itself
        ru: '',
        autoDetected: true,
        detectedAt: new Date().toISOString()
      }, { merge: true });
      console.log(`[i18n] Autodetected missing key: ${key}`);
    }
  } catch (err) {
    console.error(`[i18n] Failed to report missing key ${key}:`, err);
  }
});

